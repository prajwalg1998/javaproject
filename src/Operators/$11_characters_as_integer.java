package Operators;

public class $11_characters_as_integer {
	
	public static void main(String [] args)
	{
		char a1='A';
		char b1='B';
		System.out.println(a1+b1);
		
		char c='A';
		System.out.println(c+10);
		//character added with any arithmetic operator it acts as integer
		System.out.println(c);
		//character printed alone will act as character
	}

}
