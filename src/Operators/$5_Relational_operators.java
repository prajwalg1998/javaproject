package Operators;

public class $5_Relational_operators {

	public static void main(String[] args) {
	        /*> - greater than
	        < - less than
	        >= - greater than or equal to
	        <= - less than or equal to
	        == - double equal
	        != - not equal
	        all of them will result boolean o/p*/
	        int a = 45;
	        int c = 65;
	        boolean b = a > c;
	        boolean b1 = a < c;
	        boolean b2 = a >= c;
	        boolean b3 = a <= c;
	        boolean b4 = a == c;
	        boolean b5 = a != c;
	        System.out.println(b);
	        System.out.println(b1);
	        System.out.println(b2);
	        System.out.println(b3);
	        System.out.println(b4);
	        System.out.println(b5);

	}

}
