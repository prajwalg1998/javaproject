package Operators;

public class $2_Arithmetic_operators
{
    public static void main(String[] args)
    {
        int a=10;
        int b=20;
        float f=2.99f;
        /*Arithmeticoperators
                addition
                subtraction
                multiplication
                division*/
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a/f);
    }
}
